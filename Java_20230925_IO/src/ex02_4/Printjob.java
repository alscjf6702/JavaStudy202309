package ex02_4;

public class Printjob {
	private String color, grayscale;
	private String A4, A5, Letter, Legal;
	private int count;
	
	public Printjob(String color, String grayscale, String a4, String a5, String letter, String legal, int count) {
		super();
		this.color = color;
		this.grayscale = grayscale;
		A4 = a4;
		A5 = a5;
		Letter = letter;
		Legal = legal;
		this.count = count;
	}


}
